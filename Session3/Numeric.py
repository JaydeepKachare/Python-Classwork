# Numeric data type 
# integer 
no = 11 
print("value of no      :   " , no)
print("Data type of no  :   ",type(no))
print("Unique id of no  :   ",id(no))

# float
value = 10.30 
print("value of no      :   " , value)
print("Data type of no  :   ",type(value))
print("Unique id of no  :   ",id(value))

# complex
a = 4 + 5j 
print("value of no      :   " , a)
print("Data type of no  :   ",type(a))
print("Unique id of no  :   ",id(a))
